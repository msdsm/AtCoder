string f(ll x){
	string ret;
	ll tmp = x%16;
	x/=16;
	if(tmp>9)
		ret.push_back('A'+tmp-10);
	else
		ret.push_back('0'+tmp);
	tmp = x%16;
	if(tmp>9)
		ret.push_back('A'+tmp-10);
	else
		ret.push_back('0'+tmp);
	reverse(ret.begin(),ret.end());
	return ret;
}

